package com.taotao.portal.pojo;

public class Ad1Node {
	
	private String srcB; //张图片
	private String height;//设置高度
	private String alt;//跳转方式
	private String width; //宽度
	private String src;//图片
	private String widthB;//图片B的高度宽度
	private String href;//url路径地址
	private String heightB;//图片b的格式
	
	public String getSrcB() {
		return srcB;
	}
	public void setSrcB(String srcB) {
		this.srcB = srcB;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getAlt() {
		return alt;
	}
	public void setAlt(String alt) {
		this.alt = alt;
	}
	public String getWidth() {
		return width;
	}
	public void setWidth(String width) {
		this.width = width;
	}
	public String getSrc() {
		return src;
	}
	public void setSrc(String src) {
		this.src = src;
	}
	public String getWidthB() {
		return widthB;
	}
	public void setWidthB(String widthB) {
		this.widthB = widthB;
	}
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
	public String getHeightB() {
		return heightB;
	}
	public void setHeightB(String heightB) {
		this.heightB = heightB;
	}
	
	

}
